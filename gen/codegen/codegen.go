// Package codegen converts parsed G-rizzle schema definitions into Go source
// files containing typed table handles and model structs.
package codegen

import (
	"bytes"
	"fmt"
	"go/format"
	"path/filepath"
	"sort"
	"strings"
	"text/template"

	"github.com/sofired/grizzle/gen/parser"
)

// Options controls code generation.
type Options struct {
	PackageName string // e.g. "myschema"
	OutputDir   string // directory to write *_gen.go files into
}

// GeneratedFile holds the result for one table.
type GeneratedFile struct {
	FileName string // e.g. "users_gen.go"
	Source   []byte // formatted Go source
}

// GenerateAll processes a slice of ParsedTables and returns one GeneratedFile per table.
func GenerateAll(tables []*parser.ParsedTable, opts Options) ([]GeneratedFile, error) {
	var files []GeneratedFile
	for _, t := range tables {
		gf, err := GenerateTable(t, opts)
		if err != nil {
			return nil, fmt.Errorf("table %q: %w", t.TableName, err)
		}
		files = append(files, gf)
	}
	return files, nil
}

// GenerateTable generates the Go source for a single table.
func GenerateTable(pt *parser.ParsedTable, opts Options) (GeneratedFile, error) {
	// Resolve all columns.
	cols := make([]ColumnInfo, 0, len(pt.Columns))
	for _, pc := range pt.Columns {
		ci, err := ResolveColumn(pc)
		if err != nil {
			return GeneratedFile{}, err
		}
		cols = append(cols, ci)
	}

	// Collect imports needed, split into stdlib and third-party groups.
	td := templateData{
		PackageName:     opts.PackageName,
		ImportBlock:     buildImportBlock(cols),
		TableName:       pt.TableName,
		TableTypeName:   tableTypeName(pt.TableName),
		SingletonName:   tableSingletonName(pt.TableName),
		SelectModelName: selectModelName(pt.TableName),
		InsertModelName: insertModelName(pt.TableName),
		UpdateModelName: updateModelName(pt.TableName),
		Columns:         cols,
	}

	var buf bytes.Buffer
	if err := fileTemplate.Execute(&buf, td); err != nil {
		return GeneratedFile{}, fmt.Errorf("template execute: %w", err)
	}

	// Format with go/format for canonical style.
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return raw source with error context for debugging.
		return GeneratedFile{}, fmt.Errorf("format source:\n%s\nerror: %w", buf.String(), err)
	}

	return GeneratedFile{
		FileName: filepath.Join(opts.OutputDir, genFileName(pt.TableName)),
		Source:   formatted,
	}, nil
}

// buildImportBlock returns a fully formatted import(...) block string with
// stdlib and third-party packages in separate groups separated by a blank line.
func buildImportBlock(cols []ColumnInfo) string {
	seen := map[string]bool{
		"github.com/sofired/grizzle/expr": true,
	}
	for _, c := range cols {
		if c.NeedsImport != "" {
			seen[c.NeedsImport] = true
		}
		if c.ColType == "expr.TimestampColumn" {
			seen["time"] = true
		}
	}

	var stdlib, thirdParty []string
	for imp := range seen {
		if isStdlib(imp) {
			stdlib = append(stdlib, imp)
		} else {
			thirdParty = append(thirdParty, imp)
		}
	}
	sort.Strings(stdlib)
	sort.Strings(thirdParty)

	var b strings.Builder
	b.WriteString("import (\n")
	for _, imp := range stdlib {
		fmt.Fprintf(&b, "\t%q\n", imp)
	}
	if len(stdlib) > 0 && len(thirdParty) > 0 {
		b.WriteString("\n")
	}
	for _, imp := range thirdParty {
		fmt.Fprintf(&b, "\t%q\n", imp)
	}
	b.WriteString(")")
	return b.String()
}

// isStdlib reports whether an import path is a Go standard library package.
// Standard library paths contain no dot in their first path segment.
func isStdlib(path string) bool {
	first := path
	if i := strings.IndexByte(path, '/'); i >= 0 {
		first = path[:i]
	}
	return !strings.Contains(first, ".")
}

// templateData is passed to fileTemplate.
type templateData struct {
	PackageName     string
	ImportBlock     string // fully formatted import(...) block, ready to emit verbatim
	TableName       string
	TableTypeName   string
	SingletonName   string
	SelectModelName string
	InsertModelName string
	UpdateModelName string
	Columns         []ColumnInfo
}

// pkColName returns the name of the primary key column, or "id" as fallback.
func (td templateData) PKColName() string {
	for _, c := range td.Columns {
		if c.IsPK {
			return c.FieldName
		}
	}
	return "ID"
}

var funcMap = template.FuncMap{
	"backtick": func(s string) string { return "`" + s + "`" },
	"quote":    func(s string) string { return `"` + s + `"` },
}

var fileTemplate = template.Must(template.New("file").Funcs(funcMap).Parse(fileTemplateText))

const fileTemplateText = `// Code generated by grizzle gen. DO NOT EDIT.

package {{.PackageName}}

{{.ImportBlock}}

// ---------------------------------------------------------------------------
// Table handle: {{.TableTypeName}}
// Use {{.SingletonName}} as the singleton in queries.
// ---------------------------------------------------------------------------

// {{.TableTypeName}} provides typed column handles for the {{.TableName}} table.
// Each field is a typed column expression — use it to build WHERE clauses,
// ORDER BY expressions, and SELECT lists with compile-time type checking.
type {{.TableTypeName}} struct {
{{- range .Columns}}
	{{.FieldName}} {{.ColType}}
{{- end}}
}

func ({{.TableTypeName}}) GRizTableName() string  { return {{quote .TableName}} }
func ({{.TableTypeName}}) GRizTableAlias() string { return {{quote .TableName}} }

// {{.SingletonName}} is the singleton handle for the {{.TableName}} table.
var {{.SingletonName}} = {{.TableTypeName}}{
{{- range .Columns}}
	{{.FieldName}}: {{.ColType}}{ ColBase: expr.ColBase{TableAlias: {{quote $.TableName}}, ColName: {{quote .ColName}}}},
{{- end}}
}

// ---------------------------------------------------------------------------
// {{.SelectModelName}} — full select model for {{.TableName}}
// ---------------------------------------------------------------------------

// {{.SelectModelName}} is the destination struct for SELECT * queries on {{.TableName}}.
// Nullable columns are represented as pointer types.
type {{.SelectModelName}} struct {
{{- range .Columns}}
	{{.FieldName}} {{.SelectGoType}} {{backtick (print "db:\"" .ColName "\"")}}
{{- end}}
}

// ---------------------------------------------------------------------------
// {{.InsertModelName}} — insert model for {{.TableName}}
// ---------------------------------------------------------------------------

// {{.InsertModelName}} is the source struct for INSERT INTO {{.TableName}}.
// Fields with database defaults or nullable columns are pointer types with
// omitempty so they can be left unset.
type {{.InsertModelName}} struct {
{{- range .Columns}}
	{{.FieldName}} {{.InsertGoType}} {{backtick .InsertTag}}
{{- end}}
}

// ---------------------------------------------------------------------------
// {{.UpdateModelName}} — update model for {{.TableName}}
// ---------------------------------------------------------------------------

// {{.UpdateModelName}} is used with UpdateBuilder.SetStruct for partial updates.
// Only non-nil fields will be included in the SET clause.
type {{.UpdateModelName}} struct {
{{- range .Columns}}
{{- if not .IsPK}}
	{{.FieldName}} {{.GoTypePtr}} {{backtick (print "db:\"" .ColName "\"")}}
{{- end}}
{{- end}}
}
`

